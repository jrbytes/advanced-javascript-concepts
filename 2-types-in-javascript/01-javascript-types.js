// Tipos primitivos - apresentam apenas um valor
typeof 5
typeof true
typeof 'like'
typeof undefined
typeof null // retorna um objeto, se resolver pode corromper vários sistemas
typeof Symbol('Just me')

// Tipos não primitivos
typeof {}
// Matrizes e funções são objetos
typeof [] // retorna um objeto
typeof function(){} // as funções são objetos
